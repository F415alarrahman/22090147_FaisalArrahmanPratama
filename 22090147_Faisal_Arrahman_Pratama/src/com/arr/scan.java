    private void inputMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inputMouseClicked
        // TODO add your handling code here:
        String id = input.getText();
        try {
            String q = "SELECT * FROM member WHERE id="+id;
            Connection k = ConDB.getConnectionToDB();
            Statement s = k.createStatement();
            ResultSet resultSet = s.executeQuery(q);

            while (resultSet.next()) {          
                String username = resultSet.getString("username");
                String alamat = resultSet.getString("alamat");
                Date tanggalMulai = resultSet.getDate("tanggal_mulai");
                Date tanggalBerakhir = resultSet.getDate("tanggal_berakhir");
                labelNama.setText(username.toUpperCase());
                updateDB(id);
                addDataToTable(username, alamat, tanggalMulai,tanggalBerakhir);
                
            }
            input.setText("");
            input.requestFocus();
            
            
            
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_inputMouseClicked

