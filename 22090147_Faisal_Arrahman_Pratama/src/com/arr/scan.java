    private void updateDB(String id) {
        try {
            Connection k = ConDB.getConnectionToDB();
            Statement s = k.createStatement();
            String q = "UPDATE member "
                    + "SET "
                    + "status_member='Datang' "
                    + "WHERE id="+id;
            s.executeUpdate(q);
        } catch (Exception e) {
        }
    }

    private void addDataToTable(String username, String alamat, Date tanggalMulai, Date tanggalBerakhir) {
        try {
            DefaultTableModel m = (DefaultTableModel) tabel_data.getModel();
            int row = tabel_data.getRowCount()+1;
            Object[] dataTamu = {row,username,alamat, tanggalMulai, tanggalBerakhir,"hadir"};
            m.addRow(dataTamu); 
        } catch (Exception e) {
        }
    
    }
