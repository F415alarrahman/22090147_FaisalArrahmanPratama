    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        // TODO add your handling code here:String username = jTextField1.getText();
    String username = jTextField1.getText();
    String alamat = jTextField2.getText();
    Date date = tanggal.getDate();
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String tanggalMulaiStr = (date != null) ? sdf.format(date) : null;

    if (username.isEmpty() || alamat.isEmpty() || tanggalMulaiStr == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Username dan Tanggal tidak boleh kosong!!, Kecuali Alamat", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        Connection k = ConDB.getConnectionToDB();
        Statement s = k.createStatement();

        // Hitung tanggal berakhir satu bulan setelah tanggal mulai
        Date tanggalMulai = sdf.parse(tanggalMulaiStr);
        long satuBulanMillis = 30L * 24L * 60L * 60L * 1000L;
        Date tanggalBerakhir = new Date(tanggalMulai.getTime() + satuBulanMillis);
        String tanggalBerakhirStr = sdf.format(tanggalBerakhir);

        String q = "INSERT INTO member (username, alamat, tanggal_mulai, tanggal_berakhir) VALUES ('" + username + "','" + alamat + "','" + tanggalMulaiStr + "','" + tanggalBerakhirStr + "')";
        s.executeUpdate(q);

        // Buat objek Member dengan data yang baru ditambahkan
        Member member = new Member(username, alamat, tanggalMulaiStr, tanggalBerakhirStr);

        // Tampilkan notifikasi jika membership akan berakhir dalam satu bulan
        if (member.isMembershipExpiringSoon()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Peringatan: Membership Anda akan berakhir pada " + sdf.format(member.getTanggalBerakhir()), "Notifikasi", javax.swing.JOptionPane.WARNING_MESSAGE);
        }

        refreshTable();

    } catch (SQLException | ParseException e) {
        System.err.println("Error: " + e.getMessage());
    }
        
    }//GEN-LAST:event_jLabel10MouseClicked

