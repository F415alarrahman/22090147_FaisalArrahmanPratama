    private void refreshTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) tabel_data.getModel();
            model.setRowCount(0);

            String query = "SELECT * FROM member";
            Connection connection = ConDB.getConnectionToDB();
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery(query);

            while (resultSet.next()) {
                int id = resultSet.getInt("id");
                String username = resultSet.getString("username");
                String alamat = resultSet.getString("alamat");
                Date tanggalMulai = resultSet.getDate("tanggal_mulai");
                Date tanggalBerakhir = resultSet.getDate("tanggal_berakhir");

                if (tanggalMulai != null) {
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    String tanggalMulaiStr = sdf.format(tanggalMulai);
                    String tanggalBerakhirStr = (tanggalBerakhir != null) ? sdf.format(tanggalBerakhir) : "";

                    Object[] rowData = {id, username, alamat, tanggalMulaiStr, tanggalBerakhirStr};
                    model.addRow(rowData);
                }
            }

            resultSet.close();
            statement.close();
            connection.close();

        } catch (SQLException e) {
            System.err.println("Error: " + e.getMessage());
        }
    
}

     public class Member {
    private String username;
    private String alamat;
    private Date tanggalMulai;
    private Date tanggalBerakhir;

    public Member(String username, String alamat, String tanggalMulaiStr, String tanggalBerakhirStr) {
        this.username = username;
        this.alamat = alamat;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            this.tanggalMulai = sdf.parse(tanggalMulaiStr);
            // Hitung tanggal berakhir satu bulan setelah tanggal mulai
            this.tanggalBerakhir = new Date(this.tanggalMulai.getTime() + 30L * 24L * 60L * 60L * 1000L);
        } catch (ParseException e) {
            e.printStackTrace();
        }
    }

    public String getUsername() {
        return username;
    }

    public String getalamat() {
        return alamat;
    }
    
    public Date getTanggalMulai() {
        return tanggalMulai;
    }

    public Date getTanggalBerakhir() {
        return tanggalBerakhir;
    }

    public boolean isMembershipExpiringSoon() {
        // Mendapatkan tanggal hari ini
        Date today = new Date();

        // Mendapatkan tanggal satu bulan dari sekarang
        long satuBulanMillis = 30L * 24L * 60L * 60L * 1000L;
        Date satuBulanKedepan = new Date(today.getTime() + satuBulanMillis);

        // Memeriksa apakah sisa waktu kurang dari satu bulan
        return tanggalBerakhir.before(satuBulanKedepan);
    }
    }
    
