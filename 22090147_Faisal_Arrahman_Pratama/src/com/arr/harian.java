    private void jLabel15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel15MouseClicked
        // TODO add your handling code here:
        String username = jTextField1.getText();
        String alamat = jTextField2.getText();
        Date date = tanggal.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tanggalStr = (date != null) ? sdf.format(date) : null;
        
        
        if (username.isEmpty() || alamat.isEmpty() || tanggalStr == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Username dan alamat dan Tanggal tidak boleh kosong!!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
            }
        
        try {
            Connection k = ConDB.getConnectionToDB();
            Statement s = k.createStatement();
            String q = "INSERT INTO userharian"
                    + "(username,alamat, tanggal)"
                    + "VALUES ('" + username + "','" + alamat + "','" + tanggalStr + "')";
            s.executeUpdate(q);
           
            
            
            lastInsertedDate = date;
            refreshTable();

        } catch (SQLException e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jLabel15MouseClicked

