    private void refreshTable() {
    try {
        DefaultTableModel m = (DefaultTableModel) tabel_data.getModel();
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String lastInsertedDateStr = (lastInsertedDate != null) ? sdf.format(lastInsertedDate) : null;
        String q = "SELECT * FROM userharian WHERE tanggal = '" + lastInsertedDateStr + "'";

        Connection k = ConDB.getConnectionToDB();
        Statement s = k.createStatement();
        ResultSet r = s.executeQuery(q);

        m.getDataVector().removeAllElements();

        while (r.next()) {
            int no = r.getInt("id");
            String username = r.getString("username");
            String alamat = r.getString("alamat");
            
            // Pastikan kolom 'tanggal' tidak null sebelum mengambil nilainya
            Date tanggal = r.getDate("tanggal");
            if (tanggal != null) {
                // Format tanggal sesuai kebutuhan
                String tanggalStr = sdf.format(tanggal);

                Object[] data = {no, username, alamat, tanggalStr}; // Tambahkan kolom tanggal ke dalam data
                m.addRow(data);
            } else {
                // Tangani jika kolom tanggal null
                System.err.println("Warning: Nilai tanggal null untuk id = " + no);
            }
        }

    } catch (SQLException e) {
        // Tangani kesalahan eksekusi query
        System.out.println("SQL error: " + e.getMessage());
    } catch (Exception e) {
        // Tangani kesalahan umum
        System.out.println("Error: " + e.getMessage());
    }
    }
